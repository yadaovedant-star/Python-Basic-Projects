print("---------------\n---------------")
# c) Empty tuple using two different ways
empty_tuple1 = ()          # Method 1: Using empty parentheses
empty_tuple2 = tuple()     # Method 2: Using tuple() constructor
print("Empty Tuple 1:", empty_tuple1)
print("Type:", type(empty_tuple1))
print("Empty Tuple 2:", empty_tuple2)
print("Type:", type(empty_tuple2))
print("---------------\n---------------")
# d) Single-element tuple with the value 99
single_tuple = (99,)       # comma is required to define it as tuple
