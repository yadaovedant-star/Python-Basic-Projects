# Nested tuple representing a 3x3 matrix
matrix = (
    (2, 3, 4),   # First row
    (4, 6, 8),   # Second row
    (8, 9, 12)    # Third row
)

# a) Print the first row
print("First row:", matrix[0])

# b) Print the element at second row, third column

print("Element at second row, third column:", matrix[1][2])

# c) Print the element at third row, first column

print("Element at third row, first column:", matrix[2][0])
