import math

# Square root
Variable_1 = float(input("Enter a number for square root: "))
print("Square root:", math.sqrt(Variable_1))

# Factorial
Variable_2 = int(input("Enter a positive integer for factorial: "))
print("Factorial:", math.factorial(Variable_2))

# Ceiling and Floor
Variable_3 = float(input("Enter a number for ceil/floor: "))
print("Ceiling:", math.ceil(Variable_3))
print("Floor:", math.floor(Variable_3))
