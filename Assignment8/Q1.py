# a) A set with 5 integers
The_int_set = {7, 4, 3, 9, 0}
print("Integer set:", The_int_set)
print("Type:", type(The_int_set))

# b) A set with mixed data types (integer, string, float)
Its_mixed_set = {15, "Sarthak", 59.8}
print("\nMixed set:", Its_mixed_set)
print("Type:", type(Its_mixed_set))

# c) An empty set using the correct method
Its_empty_set = set()   # {} creates a dictionary, so we use set()
print("\nEmpty set:", Its_empty_set)
print("Type:", type(Its_empty_set))

# d) A set from the string "hello" using the set() constructor
The_string_set = set("Hello")
print("\nSet from string:", The_string_set)
print("Type:", type(The_string_set))



