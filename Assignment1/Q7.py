#Taking input from the user for length and width of the rectangle
length = float(input("Enter the Length for the rectangle: "))#Input In float
width = float(input("Enter the Width for the rectangle: "))#Input In float

Area = length * width #formula for area of rectangle
print("The area of the rectangle is:",Area)#output
Perimeter = 2 * (length + width) #formula for perimeter of rectangle
print("The perimeter of the rectangle is:",Perimeter)#output
 