a=15
b=4
print("a + b = ", a + b)# addition
print("a - b = ", a - b)# subtraction

print("a * b = ", a * b)# multiplication
print("a / b = ", a / b)# (/) division is normal division which gives float result

print("a // b = ", a // b)# (//) floor division Gives The answer in integer by removing the decimal part 
print("a % b = ", a % b)# modulo

print("a ** b = ", a ** b)# Power Multiplication
