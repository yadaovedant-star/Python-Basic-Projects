# Defining a function named greet with one parameter 'name'
def greet(name):
    print("Hello,", name, "! Welcome back.")

# Calling the function with your own name
greet("Vedant")

# Calling the function with a different name to show reusability
greet("Chinmay")
greet("Sa")
greet("Sarthak")
# Functions are useful because they allow code reuse with cleaner look
# can be called with arguments to perform specific tasks

